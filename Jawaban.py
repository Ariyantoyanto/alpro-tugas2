# Tipe data di Python dan contohnya

# 1. Integer (int)
integer_example = 10
print("Integer:", integer_example)

# 2. Float (float)
float_example = 10.5
print("Float:", float_example)

# 3. String (str)
string_example = "Hello, Python!"
print("String:", string_example)

# 4. Boolean (bool)
boolean_true = True
boolean_false = False
print("Boolean (True):", boolean_true)
print("Boolean (False):", boolean_false)

# 5. List
list_example = [1, 2, 3, "Python", 4.5]
print("List:", list_example)

# 6. Tuple
tuple_example = (1, 2, 3, "Python", 4.5)
print("Tuple:", tuple_example)

# 7. Dictionary (dict)
dict_example = {
    "name": "John",
    "age": 25,
    "is_student": True
}
print("Dictionary:", dict_example)

# 8. Set
set_example = {1, 2, 3, 4, 5}
print("Set:", set_example)

# 9. None (tipe NoneType)
none_example = None
print("None:", none_example)
